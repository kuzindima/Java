    public static int comapre(String x1, String x2) {// comapre для сравнения слов в сортировке
        x1 = x1.toLowerCase().trim();
        x2 = x2.toLowerCase().trim();
        char[] array1 = x1.toCharArray();
        char[] array2 = x2.toCharArray();

        int minLength = x1.length() > x2.length() ? x2.length() : x1.length();

        for (int i = 0; i < minLength; i++) {

            if (array1[i] < array2[i]) {
                return 1;
            } else if (array2[i] < array1[i]) {
                return 2;
            }
        }
        if (x1.length() == x2.length())
            return 0;
        else
            return x1.length() > x2.length() ? 2 : 1;

    }

